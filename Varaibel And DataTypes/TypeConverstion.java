import java.util.*;
public class TypeConverstion {
    public static void main(String[] args)
    {
        Scanner sc = new Scanner (System.in);
        int a=sc.nextInt();
        float b= a;
        System.out.println(b);


        //Type Casting


        float d=sc.nextFloat();
        int e= (int)d;
        System.out.println(e);

    }
}