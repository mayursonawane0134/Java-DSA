public class DataTypes {
    public static void main(String[] args) {
        int a = 5;
        long b = 1234567890L;
        short c = 12;
        float d = 12.3f;
        double e = 121654.1654;
        char f = 'm';
        byte g = 12;
        boolean h = true;

        System.out.println(a + " " + b + " " + c + " " + d + " " + e + " " + f + " " + g + " " + h);
    }
}
