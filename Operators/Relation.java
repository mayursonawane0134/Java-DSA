public class Relation
{
    public static void main(String[] args)
    {
        int a=10,b=20;
        System.out.println(a<b);
        int c=10,d=9;
        System.out.println(c>d);
        int e=10,f=10;
        System.out.println(e==f);
        int g=10,h=12;
        System.out.println(g<=h);
        int i=20,j=9;
        System.out.println(i>=j);
    }
}